// 8-pixel-wide font rows drawn 2× tall → 8×16 character cells.
const CHAR_W: u32 = 8;
const CHAR_H: u32 = 16;

struct FbWriter {
    addr:   u64,
    width:  u32,
    height: u32,
    pitch:  u32,
    bpp:    u32, // bytes per pixel (must be 4 for 32-bit)
    r_shl:  u8,
    g_shl:  u8,
    b_shl:  u8,
    col:    u32,
    row:    u32,
    cols:   u32,
    rows:   u32,
}

unsafe impl Send for FbWriter {}

impl FbWriter {
    fn make_pixel(&self, r: u8, g: u8, b: u8) -> u32 {
        ((r as u32) << self.r_shl) | ((g as u32) << self.g_shl) | ((b as u32) << self.b_shl)
    }

    #[inline]
    fn put_pixel(&self, x: u32, y: u32, color: u32) {
        let offset = (y * self.pitch + x * self.bpp) as u64;
        unsafe {
            ((self.addr + offset) as *mut u32).write_volatile(color);
        }
    }

    fn draw_char(&self, c: u8, col: u32, row: u32) {
        let glyph = &FONT8X8[(c & 0x7F) as usize];
        let x0 = col * CHAR_W;
        let y0 = row * CHAR_H;
        let fg = self.make_pixel(255, 255, 0);
        let bg = self.make_pixel(0, 0, 0);

        for (gy, &byte) in glyph.iter().enumerate() {
            let py = y0 + gy as u32 * 2;
            for gx in 0..8u32 {
                let color = if (byte >> gx) & 1 != 0 { fg } else { bg };
                self.put_pixel(x0 + gx, py,     color);
                self.put_pixel(x0 + gx, py + 1, color);
            }
        }
    }

    fn clear_row(&self, row: u32) {
        let y0 = row * CHAR_H;
        unsafe {
            core::ptr::write_bytes(
                (self.addr + (y0 * self.pitch) as u64) as *mut u8,
                0,
                (CHAR_H * self.pitch) as usize,
            );
        }
    }

    fn scroll_up(&self) {
        for row in 1..self.rows {
            let src = (self.addr + ((row       * CHAR_H) * self.pitch) as u64) as *const u8;
            let dst = (self.addr + (((row - 1) * CHAR_H) * self.pitch) as u64) as *mut u8;
            unsafe {
                core::ptr::copy_nonoverlapping(src, dst, (CHAR_H * self.pitch) as usize);
            }
        }
        self.clear_row(self.rows - 1);
    }

    fn write_byte(&mut self, byte: u8) {
        match byte {
            b'\n' => {
                self.col = 0;
                self.row += 1;
                if self.row >= self.rows {
                    self.scroll_up();
                    self.row = self.rows - 1;
                }
            }
            b => {
                if self.col >= self.cols {
                    self.write_byte(b'\n');
                }
                self.draw_char(b, self.col, self.row);
                self.col += 1;
            }
        }
    }
}

impl fmt::Write for FbWriter {
    fn write_str(&mut self, s: &str) -> fmt::Result {
        for b in s.bytes() {
            match b {
                0x20..=0x7e | b'\n' => self.write_byte(b),
                _ => self.write_byte(b'?'),
            }
        }
        Ok(())
    }
}

lazy_static! {
    static ref WRITER: Mutex<Option<FbWriter>> = Mutex::new(None);
}

pub fn init(fb: &Framebuffer) {
    if fb.bpp != 32 {
        return; // only 32-bit framebuffers supported
    }
    let pitch  = fb.pitch as u32;
    let width  = fb.width as u32;
    let height = fb.height as u32;
    let addr   = fb.address() as u64;

    // Clear to black (0 works for any component layout)
    unsafe {
        core::ptr::write_bytes(addr as *mut u8, 0, (height * pitch) as usize);
    }

    *WRITER.lock() = Some(FbWriter {
        addr,
        width,
        height,
        pitch,
        bpp:   4,
        r_shl: fb.red_mask_shift,
        g_shl: fb.green_mask_shift,
        b_shl: fb.blue_mask_shift,
        col:   0,
        row:   0,
        cols:  width  / CHAR_W,
        rows:  height / CHAR_H,
    });
}

#[doc(hidden)]
pub fn _print(args: fmt::Arguments) {
    use core::fmt::Write;
    use x86_64::instructions::interrupts;
    interrupts::without_interrupts(|| {
        if let Some(w) = WRITER.lock().as_mut() {
            w.write_fmt(args).unwrap();
        }
    });
}
